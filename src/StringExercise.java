public class StringExercise {
    public static void main(String[] args) {
        String PinkFloyd = "We don't need no education\n" +
                "We don't need no thought control";
        System.out.println(PinkFloyd);

        String weird = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(weird);

        String windows = "In windows, the main drive is usually C:\\\n";
        System.out.println(windows);

        String showoff = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(showoff);
    }
}
